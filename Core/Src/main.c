uint8_t crc8(uint8_t *data, uint8_t len) {
    uint8_t crc = 0x00;
    for (uint8_t i = 0; i < len; i++) {
        crc ^= data[i];
        for (uint8_t j = 0; j < 8; j++) {
            if (crc & 0x80)
                crc = (crc << 1) ^ 0x31;
            else
                crc <<= 1;
        }
    }
    return crc;
}

void make_lora_packet(uint8_t *buf, float temp_f, float hum_f){
	int16_t temp = temp_f*100;
	uint16_t hum = hum_f*100;

	//0-1 byte temperature
	buf[0] = (temp >> 8) & 0xFF;
	buf[1] = temp & 0xFF;

	//2-3 byte humidity
	buf[2] = (hum >> 8) & 0xFF;
	buf[3] = hum & 0xFF;

	//4 byte MC ID
	buf[4] = MC_ID;

	//5 byte flags
	buf[5] = 0;
	//...

	//6 byte CRC
	buf[6] = crc8(buf,6);

}

